a = int(input("Enter the first number:"))
b = int(input("Enter the second number:"))

great = a > b
print("{0} > {1} is {2}".format(a, b, great))

less = a < b
print("{0} < {1} is {2}".format(a, b, less))

eql = a == b
print("{0} == {1} is {2}".format(a, b, eql))

neql = a != b
print("{0} != {1} is {2}".format(a, b, neql))

greql = a >= b
print("{0} >= {1} is {2}".format(a, b, greql))

leseql = a <= b
print("{0} <= {1} is {2}".format(a, b, leseql))
